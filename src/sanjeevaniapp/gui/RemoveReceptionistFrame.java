    private void loadReceptionistId() {
        try{
            List<String>recepList = ReceptionistDao.getAllReceptionistId();
            for(String recId:recepList){
                jcReceptionistId.addItem(recId);
            }
            if(jcReceptionistId.getItemCount()!=0)
                 btnDelete.setEnabled(true);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In RemoveReceptionistFrame in DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
