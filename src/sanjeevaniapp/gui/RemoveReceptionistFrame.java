    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String recId = jcReceptionistId.getSelectedItem().toString();
        if(jcReceptionistId.getItemCount()==0){
            btnDelete.setEnabled(false);
            JOptionPane.showMessageDialog(null, "All receptionists removed!");
            return;
        }
        try{
            boolean result = ReceptionistDao.deleteReceptionistById(recId);
            if(result){
                JOptionPane.showMessageDialog(null,"Receptionist removed successfully!!","Success!",JOptionPane.INFORMATION_MESSAGE);
                jcReceptionistId.removeItem(recId);
                jcReceptionistId.setSelectedIndex(0);
                if(jcReceptionistId.getItemCount()==0){
                    btnDelete.setEnabled(false);
                    JOptionPane.showMessageDialog(null,"All receptionists removed!");
                }
            }else{
                    JOptionPane.showMessageDialog(null, "Deletion unsuccessful","Error!",JOptionPane.ERROR_MESSAGE); 
                }
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Error in RemoveReceptionistFrame in DB : "+ex.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

