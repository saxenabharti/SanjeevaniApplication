    private boolean isValidOTP() {
        boolean result = false;
        try{
            int otp = Integer.parseInt(txtEnterOTP.getText().trim());
            System.out.println(otp);
            System.out.println(patient.getOtp());
            if(otp == patient.getOtp()){
                result = true;
            }
                
        }catch(NumberFormatException ex ){
            JOptionPane.showMessageDialog(null,"Invalid OTP","Error",JOptionPane.ERROR_MESSAGE);
        }finally{
            System.out.println("otp not match");
            return result;
        }
    }
    private boolean isContactNoValid() {
        mobNo = txtPhoneNo.getText().trim();
        char[] mno = mobNo.toCharArray();
        for(char ch:mno){
            if(Character.isDigit(ch)==false)
                return false;
        }
        if(mno.length==10)
            return true;
        return false;
    }
