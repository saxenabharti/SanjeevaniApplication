    private void btnSendOTPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendOTPActionPerformed
        if(!isContactNoValid()){
            JOptionPane.showMessageDialog(null,"Invalid Mobile Number","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        System.out.println("After contact valid ");
        patient.setMno(mobNo);
        try{
           Sender sender = new OTPSender();
           String pOtp = String.valueOf(patient.getOtp());
           boolean result = sender.send(patient.getMno(), pOtp);
           if(result){
               JOptionPane.showMessageDialog(null, "OTP sent successfully","Success",JOptionPane.INFORMATION_MESSAGE);
           }
           else{
               JOptionPane.showMessageDialog(null,"Cannot send the OTP . Please check the number! ","Error",JOptionPane.ERROR_MESSAGE);
           }
       }catch(Exception ex){
           JOptionPane.showMessageDialog(null,"Server issue :"+ ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
       }
    }//GEN-LAST:event_btnSendOTPActionPerformed

    private void btnVerifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifyActionPerformed
        if(!isValidOTP())
            return;
        
        try{
            boolean result1 =  PatientDao.addPatient(patient);
            AppointmentPojo appt = new AppointmentPojo();
            appt.setMobileNo(patient.getMno());
            appt.setPatientId(patient.getPatientId());
            appt.setPatientName(txtPatientName.getText().trim());
            appt.setOpd(patient.getOpd());
            appt.setStatus("REQUEST");
            appt.setAppointmentDate(patient.getDate().toString());
            String docName = DoctorsDao.getDoctorNameById(patient.getDoctorId());
            appt.setDoctorName(docName);
            boolean result2 =  AppointmentDao.addAppointment(appt);
            System.out.println("result1 = "+ result1);
            System.out.println("result2 = "+ result2);
            if(result1 && result2){
                JOptionPane.showMessageDialog(null,"Patient Record Added Successfully","Success",JOptionPane.INFORMATION_MESSAGE);
                ViewAllPatientFrame frame = new ViewAllPatientFrame();
                this.dispose();
                frame.setVisible(true);
                return;
            }
            JOptionPane.showMessageDialog(null,"Could not add patient record ","Error",JOptionPane.ERROR_MESSAGE);
        }catch(SQLException ex){
            JOptionPane.showConfirmDialog(null,"Error in VerifyOtpFrame in DB : "+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnVerifyActionPerformed

