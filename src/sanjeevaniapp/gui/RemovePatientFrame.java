    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       String patId = jcPatientId.getSelectedItem().toString();
        if(jcPatientId.getItemCount()==0){
              btnDelete.setEnabled(false);
              JOptionPane.showMessageDialog(null, "All patients removed!");
              return;
                }
        try{
            boolean result = PatientDao.deletePatientById(patId);
            if(result){
                JOptionPane.showMessageDialog(null,"Patient removed successfully!!","Success!",JOptionPane.INFORMATION_MESSAGE);
                jcPatientId.removeItem(patId);
                jcPatientId.setSelectedIndex(0);
             
                if(jcPatientId.getItemCount()==0){
                    btnDelete.setEnabled(false);
                    JOptionPane.showMessageDialog(null,"All patients removed!");
                }
            }else{
                    JOptionPane.showMessageDialog(null, "Deletion unsuccessful","Error!",JOptionPane.ERROR_MESSAGE); 
                }
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Error in DeleteDoctorsFrame in DB : "+ex.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

