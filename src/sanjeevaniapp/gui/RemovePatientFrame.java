    private void loadPatientId() {
        try{
            List<String>patList = PatientDao.getAllPatientId();
            for(String pat:patList){
                jcPatientId.addItem(pat);
            }
            if(jcPatientId.getItemCount()!=0)
                btnDelete.setEnabled(true);
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Error in DeletePatientFrame in DB : "+ex.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }
