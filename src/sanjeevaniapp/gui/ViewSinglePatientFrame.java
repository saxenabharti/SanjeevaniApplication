    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String selPatId = jcPatId.getSelectedItem().toString();
        if(jcPatId.getItemCount()==0)
            btnSearch.setEnabled(false);
        try{
            PatientPojo pat = PatientDao.getPatientDetails(selPatId);
            txtOPD.setText(pat.getOpd());
            txtDocId.setText(pat.getDoctorId());
            txtAge.setText(String.valueOf(pat.getAge()));
            txtCity.setText(pat.getCity());
            txtAddress.setText(pat.getAddress());
            txtFirstName.setText(pat.getFirstName());
            txtLastName.setText(pat.getLastName());
            jcGender.setSelectedItem(pat.getGender());
            txtDate.setText(String.valueOf(pat.getDate()));
            txtPhoneNo.setText(pat.getMno());
            jcStatus.setSelectedItem(pat.getMarriedStatus());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In ViewSinglePatientFrame in DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

