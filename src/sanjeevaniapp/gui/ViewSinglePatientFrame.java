    private void loadPatientId() {
        try{
            patList = PatientDao.getAllPatientId();
            for(String patId : patList){
                jcPatId.addItem(patId);
            }
            System.out.println("jcPatId.getItemCount() = " +jcPatId.getItemCount());
            if(jcPatId.getItemCount()!=0)
                btnSearch.setEnabled(true);
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In ViewSinglePatientFrame in DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
