    private void loadDoctorDetails() {
        try{
            unRegDocList = EmpDao.getUnRegisteredDoctors();
            Set<String> empIdSet = unRegDocList.keySet();
            for(String empId: empIdSet){
                jcEmpId.addItem(empId);
            }
            if(jcEmpId.getItemCount()==0){
                btnRegister.setEnabled(false);
                JOptionPane.showMessageDialog(null,"No employee found ");
               
            }
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Error in RegisterDoctorsFrame in DB : "+ex.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }

