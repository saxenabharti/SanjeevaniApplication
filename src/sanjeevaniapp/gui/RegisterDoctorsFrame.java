    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(validateInputs()==false){
            JOptionPane.showMessageDialog(null,"Please input all fields!","Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String passwordValid = matchPassword();
        if(passwordValid!=null){
            JOptionPane.showMessageDialog(null,passwordValid,"Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(isContactNoValid()==false){
            JOptionPane.showMessageDialog(null,"Invalid Mobile number","Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            DoctorsPojo doc = new DoctorsPojo();
            doc.setDoctorId(txtDoctorId.getText());
            doc.setDoctorName(selEmpName);
            doc.setEmailId(emailId);
            doc.setGender(jcGender.getSelectedItem().toString());
            doc.setContactNo(contactNo);
            doc.setQualification(qualification);
            doc.setSpecialist(specialist);
            UserPojo user = new UserPojo();
            user.setLoginId(logID);
            String encPassword = new String(PasswordEncryption.getEncryptedPassword(pwd1));
            user.setPassword(encPassword);
            user.setUserName(selEmpName);
            user.setUsertype("Doctor");
            boolean result1 = DoctorsDao.addDoctor(doc);
            boolean result2 = UserDao.addUser(user);
            if(result1==true && result2==true){
                JOptionPane.showMessageDialog(null,"Doctor successfully registered!","Success!",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
                genNewDocId();
                return;
            }
            JOptionPane.showMessageDialog(null,"Registration unsuccessful!","Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in RegisterDoctorsFrame in DB : "+ex.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

