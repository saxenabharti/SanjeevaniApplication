    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
       if(validateInputs()==false){
            JOptionPane.showMessageDialog(null,"Please input all fields!","Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String passwordValid = matchPassword();
        if(passwordValid!=null){
            JOptionPane.showMessageDialog(null,passwordValid,"Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            ReceptionistPojo rec = new ReceptionistPojo();
            rec.setReceptionistName(selEmpName);
            rec.setReceptionistId(txtReceptionistId.getText());
            rec.setGender(jcGender.getSelectedItem().toString());
            UserPojo user = new UserPojo();
            user.setLoginId(loginId);
            String encPassword = new String(PasswordEncryption.getEncryptedPassword(pwd1));
            user.setPassword(encPassword);
            user.setUserName(selEmpName);
            user.setUsertype("Receptionist");
            boolean result1 = ReceptionistDao.addReceptionist(rec);
            boolean result2 = UserDao.addUser(user);
            if(result1==true && result2==true){
                JOptionPane.showMessageDialog(null,"Receptionist successfully registered!","Success!",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
                genNewRecepId();
                return;
            }
            JOptionPane.showMessageDialog(null,"Registration unsuccessful!","Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in RegisterReceptionistFrame in DB : "+ex.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

