    private void loadReceptionistDetails() {
        try{
            unRegRecList = EmpDao.getUnRegisteredReceptionists();
            Set<String> empIdSet = unRegRecList.keySet();
            for(String empId: empIdSet){
                jcEmpId.addItem(empId);
            }
            if(jcEmpId.getItemCount()==0){
                btnRegister.setEnabled(false);
                JOptionPane.showMessageDialog(null,"No employee found ");
                
            }
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Error in RegisterReceptionistFrame in DB : "+ex.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }

    private void genNewRecepId() {
        try{
           txtReceptionistId.setText(ReceptionistDao.getNewRecId());
       }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Error in RegisterReceptionistFrame in DB : "+ex.getMessage(),"Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }
    private boolean validateInputs(){
        loginId = txtLoginId.getText().trim();
        char[] p1 = txtPassword.getPassword();
        char [] p2 = txtRetypePassword.getPassword();
        if(loginId.isEmpty()||p1.length==0||p2.length==0)
        return false;
        pwd1 = new String(p1).trim();
        pwd2 = new String(p2).trim();
        return true;
    }
    private String matchPassword(){
        if(pwd1.length()<3||pwd2.length()<3)
             return "Passwords must be of atleast 3 characters in length";
        if(pwd1.equals(pwd2)==false)
            return "Passwords do not match";
        return null;    
    }
    private void clearAll(){
        txtLoginId.setText("");
        txtPassword.setText("");
        txtRetypePassword.setText("");
        unRegRecList.remove(selEmpId);
        jcEmpId.removeItem(selEmpId);
        if(unRegRecList.size()==0){
          JOptionPane.showMessageDialog(null,"All receptionists registered!");
          ManageReceptionist manageRec=new ManageReceptionist();
          manageRec.setVisible(true);
          this.dispose();
        }
    }
