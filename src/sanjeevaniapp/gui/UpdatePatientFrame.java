    private void loadPatientId() {
        try{
            List<String> patIdList = PatientDao.getAllPatientId();
            for(String id:patIdList){
                jcPatId.addItem(id);
        }
    }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In UpdatePatientFrame in DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

    private boolean valideInputs() {
        patient = new PatientPojo();
        patient.setOpd(txtOPD.getText().trim());
        patient.setPatientId(jcPatId.getSelectedItem().toString());
        patient.setCity(txtCity.getText().trim());
        patient.setAddress(txtAddress.getText().trim());
        patient.setFirstName(txtFirstName.getText().trim());
        patient.setLastName(txtLastName.getText().trim());
        patient.setGender(jcGender.getSelectedItem().toString());
        patient.setMarriedStatus(jcStatus.getSelectedItem().toString());
        patient.setDoctorId(txtDocId.getText());
        patient.setMno(txtPhoneNo.getText().trim());
        if(patient.getAddress().isEmpty()||patient.getCity().isEmpty()||patient.getFirstName().isEmpty()||patient.getLastName().isEmpty()||patient.getOpd().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill all the fields ","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        try{
            patient.setAge(Integer.parseInt(txtAge.getText().trim()));
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Invalid Age","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(!isContactNoValid()){
            JOptionPane.showMessageDialog(null, "Invalid Mobile Number","Error",JOptionPane.ERROR_MESSAGE);
            return false;
        } 
        return true;
    }
    private boolean isContactNoValid() {
        char[] mno = patient.getMno().toCharArray();
        for(char ch:mno){
            if(Character.isDigit(ch)==false)
                return false;
        }
        if(patient.getMno().length()==10)
            return true;
        return false;
    }
