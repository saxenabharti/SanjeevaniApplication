    private void jcPatIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcPatIdActionPerformed
        try{
            String patId = jcPatId.getSelectedItem().toString();
            PatientPojo pat = PatientDao.getPatientDetails(patId);
            txtOPD.setText(pat.getOpd());
            txtDocId.setText(pat.getDoctorId());
            txtAge.setText(String.valueOf(pat.getAge()));
            txtCity.setText(pat.getCity());
            txtAddress.setText(pat.getAddress());
            txtFirstName.setText(pat.getFirstName());
            txtLastName.setText(pat.getLastName());
            jcGender.setSelectedItem(pat.getGender().toUpperCase());
            txtDate.setText(String.valueOf(pat.getDate()));
            txtPhoneNo.setText(pat.getMno());
            jcStatus.setSelectedItem(pat.getMarriedStatus().toUpperCase());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In UpdatePatientFrame in DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcPatIdActionPerformed

